start:
	mov ax, 03h 			;Clear the screen.
	int 10h				;BIOS interrupt call.
	mov ah, 0Eh 			;BIOS service for printing a character
	mov al, "K"			
	int 0x10			;BIOS interrupt call.
	jmp $				;halt

times 510 - ($-$$) db 0		;fill the rest of the file with zeros.
